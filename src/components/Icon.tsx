/** Ikon yang bisa dipilih untuk sebuah menu/produk. */
export const PRODUCT_ICONS: IconName[] = ["coffee", "milk", "cup", "cookie", "utensils"];

const EMOJI_TO_ICON: Record<string, IconName> = {
  "☕": "coffee",
  "🫗": "coffee",
  "🥛": "milk",
  "🧋": "cup",
  "🍵": "cup",
  "🍫": "cup",
  "🥤": "cup",
  "🧊": "cup",
  "🥐": "utensils",
  "🍞": "utensils",
  "🍟": "utensils",
  "🍪": "cookie",
  "🍰": "cookie",
};

/** Ubah nilai `emoji` (bisa emoji lama atau key ikon baru) menjadi IconName. */
export function resolveProductIcon(value?: string | null): IconName {
  if (!value) return "coffee";
  if ((PRODUCT_ICONS as string[]).includes(value)) return value as IconName;
  return EMOJI_TO_ICON[value] ?? "coffee";
}

/** Ikon produk siap render dengan latar bulat lembut. */
export function ProductIcon({ value, className }: { value?: string | null; className?: string }) {
  return <Icon name={resolveProductIcon(value)} className={className ?? "h-6 w-6"} />;
}
